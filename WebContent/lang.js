const setLanguage = (language) => {
	sessionStorage.setItem("language", language);
	switch (language) {
		case "it":
			LANG = langIt;
			break;
		case "es":
			LANG = langEs;
			break;
		case "fr":
			LANG = langFr;
			break;
		case "de":
			LANG = langDe;
			break;
		default:
			LANG = langEn;
	}
}